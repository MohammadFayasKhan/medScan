"""
export_utils.py
===============
This module provides data export functionality for the MedScan AI app.

Why in-memory export?
  Streamlit's st.download_button requires data as a string or bytes object.
  We avoid writing to actual disk files because:
  - It keeps the app stateless (no temp file cleanup needed)
  - Works correctly whether the app is run locally or hosted on a server
  - Avoids file permission issues on different operating systems

What can be exported:
  - Medicine data as CSV (single medicine or full database)
  - Medicine data as JSON (structured, for use with other tools)
  - Chatbot conversation transcript as plain text

Author:  Mohammad Fayas Khan
Course:  INT428 — AI Systems Design
Version: 1.0.0
"""


import csv
import json
import io
from datetime import datetime
import logging

import streamlit as st

logger = logging.getLogger(__name__)


# ─────────────────────────────────────────────────────────────────────
# CSV EXPORT
# ─────────────────────────────────────────────────────────────────────

def medicine_to_csv_string(medicine: dict) -> str:
    """
    Convert a medicine data dict to a CSV-formatted string for download.

    list-type fields are joined with a pipe character '|' to avoid
    conflicting with CSV commas.

    Args:
        medicine (dict): Full medicine data dictionary from database.

    Returns:
        str: CSV-formatted string with header row and one data row.

    Example:
        >>> csv_str = medicine_to_csv_string(paracetamol_dict)
        >>> csv_str.startswith("name,")
        True
    """
    if not medicine:
        return ""

    # Use StringIO as an in-memory buffer for csv.writer
    buffer = io.StringIO()
    writer = csv.writer(buffer)

    # Write header row (column names)
    writer.writerow(list(medicine.keys()))

    # Write data row — flatten list values to pipe-separated strings
    row_values = []
    for val in medicine.values():
        if isinstance(val, list):
            # Join list items with pipe separator (avoids CSV comma conflict)
            row_values.append(" | ".join(str(v) for v in val))
        else:
            row_values.append(str(val) if val is not None else "")

    writer.writerow(row_values)

    # Return the full CSV string content
    return buffer.getvalue()


# ─────────────────────────────────────────────────────────────────────
# JSON EXPORT
# ─────────────────────────────────────────────────────────────────────

def medicine_to_json_string(medicine: dict) -> str:
    """
    Convert a medicine data dict to a formatted JSON string for download.

    Handles non-serialisable types (converts to strings) and formats
    with 2-space indentation for readability.

    Args:
        medicine (dict): Full medicine data dictionary from database.

    Returns:
        str: Pretty-printed JSON string.

    Example:
        >>> json_str = medicine_to_json_string(paracetamol_dict)
        >>> '"name"' in json_str
        True
    """
    if not medicine:
        return "{}"

    # Create a serialisable copy (handle numpy types, etc.)
    safe_dict = {}
    for k, v in medicine.items():
        if isinstance(v, list):
            safe_dict[k] = [str(item) for item in v]
        elif hasattr(v, "item"):
            # NumPy scalar types → native Python
            safe_dict[k] = v.item()
        else:
            safe_dict[k] = str(v) if v is not None else ""

    return json.dumps(safe_dict, indent=2, ensure_ascii=False)


