# CHAT HISTORY EXPORT
# ─────────────────────────────────────────────────────────────────────

def chat_history_to_text(history: list, medicine_name: str) -> str:
    """
    Convert a chat history list to a plain-text transcript for download.

    Format:
      ════════════════
      MEDSCAN AI — Chat Transcript
      Medicine: {name}
      Date: {date}
      ════════════════
      [USER]: message
      [MEDBOT]: response
      Detected Intent: {intent}
      ...

    Args:
        history (list[dict]): Chat history list from st.session_state.chat_history.
                              Each entry: {role, text, intent (optional)}.
        medicine_name (str): Name of the medicine being discussed.

    Returns:
        str: Plain-text formatted chat transcript.

    Example:
        >>> transcript = chat_history_to_text(history, "paracetamol")
        >>> "MEDSCAN AI" in transcript
        True
    """
    if not history:
        return "No chat history available."

    # Header block
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    lines = [
        "=" * 60,
        "  MEDSCAN AI — Chat Transcript",
        f"  Medicine: {medicine_name.title()}",
        f"  Date: {timestamp}",
        "  Generated by: MEDSCAN AI (Offline Mode)",
        "=" * 60,
        "",
    ]

    # Message blocks
    for i, msg in enumerate(history, 1):
        role = msg.get("role", "unknown").upper()
        text = msg.get("text", "")
        intent = msg.get("intent", "")

        # Format role label
        role_label = "[USER]" if role == "USER" else "[MEDBOT]"
        lines.append(f"{role_label}:")
        lines.append(text)

        # Show detected intent for bot messages
        if role == "BOT" and intent and intent != "unknown":
            lines.append(f"  (Detected Topic: {intent.replace('_', ' ').title()})")

        lines.append("")  # blank line between messages

    # Footer disclaimer
    lines += [
        "-" * 60,
        "DISCLAIMER: This transcript is for informational purposes only.",
        "It does not constitute medical advice. Consult a qualified",
        "healthcare professional before acting on any information here.",
        "-" * 60,
    ]

    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────
# STREAMLIT DOWNLOAD BUTTON
# ─────────────────────────────────────────────────────────────────────

def create_download_button(data: str, filename: str,
                           mime: str, label: str) -> None:
    """
    Render a styled Streamlit download button for in-memory data.

    Uses st.download_button — no server storage, purely client-side.
    The button renders inline at the current Streamlit position.

    Args:
        data (str): String data to be downloaded (CSV, JSON, or text).
        filename (str): Suggested filename for the downloaded file.
        mime (str): MIME type string (e.g., "text/csv", "application/json").
        label (str): Button label text displayed to the user.

    Example:
        >>> create_download_button(csv_str, "paracetamol.csv", "text/csv", "⬇ Download CSV")
    """
    # Encode string to bytes for the download button
    data_bytes = data.encode("utf-8")

    # Render styled download button via Streamlit
    st.download_button(
        label=label,
        data=data_bytes,
        file_name=filename,
        mime=mime,
        help=f"Click to download {filename}",
        use_container_width=False,
    )
